package com.smkrevit.myphotograph.Model;

public class ModelPhotograph {
    private String toko;
    private String pukul;
    private String alamattoko;
    private String rating;
    private String imgtoko;

    public String getToko() {
        return toko;
    }

    public void setToko(String toko) {
        this.toko = toko;
    }

    public String getPukul() {
        return pukul;
    }

    public void setPukul(String pukul) {
        this.pukul = pukul;
    }

    public String getAlamattoko() {
        return alamattoko;
    }

    public void setAlamattoko(String alamattoko) {
        this.alamattoko = alamattoko;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getImgtoko() {
        return imgtoko;
    }

    public void setImgtoko(String imgtoko) {
        this.imgtoko = imgtoko;
    }
}
